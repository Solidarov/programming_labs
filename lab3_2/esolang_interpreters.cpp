#include <iostream>
#include <string>
using namespace std;

std::string my_first_interpreter(const std::string& code)
{
    string result;
    size_t count = 0;
    unsigned char ch = 0;

    for (char c : code){
        if (c == '+'){
            count++;
        }
        if(c == '.'){
            ch = (ch + count) % 256;
            result += ch;
            count = 0;
        }
    }

  return result;
}

