#include <iostream>
#include <string>
using namespace std;

string my_first_interpreter(const string& code)
{
    string result;
    size_t count = 0;

    for (char c : code){
        if (c == '+'){
            count++;
        }
        if(c == '.'){
            count = count % 256;
            result += char(count);
        }
    }

  return result;
}

